#include "header.h"

int	ft_valid2(char **str)
{
	int n[3];

	n[1] = -1;
	while (str[++(n[1])])
	{
		n[2] = 0;
		n[0] = -1;
		while (str[n[1]][++(n[0])])
			if (str[n[1]][n[0]] == '#')
			{
				if (str[n[1]][(n[0]) + 1] == '#')
					(n[2])++;
				if (n[0] > 0 && str[n[1]][(n[0]) - 1] == '#')
					(n[2])++;
				if (n[0] < 14 && str[n[1]][(n[0]) + 5] == '#')
					(n[2])++;
				if (n[0] > 4 && str[n[1]][(n[0]) - 5] == '#')
					(n[2])++;
			}
		if (n[2] != 6 && n[2] != 8)
			return (1);
	}
	return (0);
}
